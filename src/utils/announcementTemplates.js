const TEMPLATE_MAP = new Map(ANNOUNCEMENT_TEMPLATES.map((template) => [template.key, template]));

const normalizePlacementValue = (value) => {
  const normalized = String(value || '').trim().toUpperCase();
  if (normalized === 'HEADER_DROPDOWN') return 'CATEGORY_DROPDOWN';
  return normalized;
};

const isTemplateCompatible = (template, context = {}) => {
  if (!template) return false;
  const location = normalizePlacementValue(context.displayLocation);
  const style = normalizePlacementValue(context.displayStyle);

  if (location && normalizePlacementValue(template.displayLocation) !== location) {
    return false;
  }

  if (style && normalizePlacementValue(template.displayStyle) !== style) {
    return false;
  }

  return true;
};

