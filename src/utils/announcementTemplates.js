export function getDefaultAnnouncementTemplateKey(displayLocation = 'HEADER', displayStyle = 'BANNER') {
  const normalizedLocation = normalizePlacementValue(displayLocation) || 'HEADER';
  let normalizedStyle = normalizePlacementValue(displayStyle) || 'BANNER';

  if (normalizedLocation === 'HEADER' || normalizedLocation === 'SECTION' || normalizedLocation === 'CATEGORY_DROPDOWN') {
    normalizedStyle = 'BANNER';
  }

  return DEFAULT_TEMPLATE_KEYS[`${normalizedLocation}:${normalizedStyle}`] || 'global-support-banner';
}

export function getAnnouncementTemplate(templateKey, context = {}) {
  if (templateKey && TEMPLATE_MAP.has(templateKey)) {
    const template = TEMPLATE_MAP.get(templateKey);
    if (isTemplateCompatible(template, context)) {
      return template;
    }
    if (!context.displayLocation && !context.displayStyle) {
      return template;
    }
  }

  const fallbackTemplate = TEMPLATE_MAP.get(
    getDefaultAnnouncementTemplateKey(context.displayLocation, context.displayStyle)
  );

  if (isTemplateCompatible(fallbackTemplate, context)) {
    return fallbackTemplate;
  }

  const compatibleTemplate = ANNOUNCEMENT_TEMPLATES.find((template) => isTemplateCompatible(template, context));
  return compatibleTemplate || fallbackTemplate || ANNOUNCEMENT_TEMPLATES[0];
}

export function resolveAnnouncementTemplateKey(announcement = {}) {
  return getAnnouncementTemplate(announcement.templateKey, announcement).key;
}

export function createAnnouncementFromTemplate(templateKey, overrides = {}) {
  const template = getAnnouncementTemplate(templateKey);
  return {
    ...template.defaults,
    templateKey: template.key,
    ...overrides,
  };
}

export function getAnnouncementTemplatesForGroup(group = 'all') {
  if (!group || group === 'all') {
    return ANNOUNCEMENT_TEMPLATES;
  }

  return ANNOUNCEMENT_TEMPLATES.filter((template) => template.group === group);
}

export function getAnnouncementTemplatesForPlacement(displayLocation, displayStyle) {
  if (!displayLocation) {
    return ANNOUNCEMENT_TEMPLATES;
  }

  return ANNOUNCEMENT_TEMPLATES.filter((template) => (
    template.displayLocation === displayLocation
    && (!displayStyle || template.displayStyle === displayStyle)
  ));
}
